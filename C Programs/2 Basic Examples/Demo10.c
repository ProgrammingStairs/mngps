/*
    ASCII VALUE 
    (American Standard Code for Information Interchange)

    A = 65    a = 97     0 = 48
    B = 66    b = 98     1 = 49  
    |    |    |    |     |    |
    Z = 90    z = 122    9 = 57
*/

#include<stdio.h>
int main(){
    printf("character : %c\n",65);
    printf("character : %c\n",90);
    printf("character : %c\n",42);
    printf("character : %c\n",45);
  
    printf("Ascii value : %d\n",'A');
    printf("Ascii value : %d\n",'Z');
    printf("Ascii value : %d\n",'*');
    printf("Ascii value : %d\n",'-');
  
    return 0;
}
