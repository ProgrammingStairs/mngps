// example showing the concept of switch control statement
#include<stdio.h>
int main(){
    switch(0){
        case 0 : 
            printf("statement 1\n");
        case 1 : 
            printf("statement 2\n");
        case 2 : 
            printf("statement 3\n");
        default : 
            printf("Invalid\n");
    }
    return 0;
}